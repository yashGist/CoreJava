package com.shape;

public class Shape {

	private String color = "";
	private int border = 0;

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public int getBorderWidth() {
		return border;
	}

	public void setBorderWidth(int borderWidth) {
		border = borderWidth;
	}

}
